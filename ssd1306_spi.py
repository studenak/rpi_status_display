class SSD1306_SPI:
    def __init__(self, spi_bus=0, spi_device=0, pin_dc=24, pin_rst=25, gpio_chip='gpiochip4'):
        self.pin_dc_num = pin_dc
        self.pin_rst_num = pin_rst
        
        # Inicializace SPI
        self.spi = spidev.SpiDev()
        self.spi.open(spi_bus, spi_device)
        self.spi.max_speed_hz = 8000000

        # Inicializace GPIO přes gpiod
        self.chip = gpiod.Chip(gpio_chip)
        self.line_dc = self.chip.get_line(self.pin_dc_num)
        self.line_rst = self.chip.get_line(self.pin_rst_num)
        
        self.line_dc.request(consumer="SSD1306_DC", type=gpiod.LINE_REQ_DIR_OUT)
        self.line_rst.request(consumer="SSD1306_RST", type=gpiod.LINE_REQ_DIR_OUT)
        
        self.buffer = [0x00] * 1024
        self._init_display()

    def _command(self, cmd):
        self.line_dc.set_value(0)
        self.spi.writebytes([cmd])

    def _data(self, data):
        self.line_dc.set_value(1)
        self.spi.writebytes(data)

    def _init_display(self):
        self.line_rst.set_value(0)
        time.sleep(0.1)
        self.line_rst.set_value(1)
        time.sleep(0.1)

        commands = [
            0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40,
            0x8D, 0x14, 0x20, 0x02, 0xA1, 0xC8, 0xDA, 0x12,
            0x81, 0xCF, 0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF
        ]
        for cmd in commands:
            self._command(cmd)

    def clear(self):
        self.buffer = [0x00] * 1024

    def flush(self):
        for page in range(8):
            self._command(0xB0 + page)
            self._command(0x00)
            self._command(0x10)
            self._data(self.buffer[page*128 : (page+1)*128])

    def draw_text(self, x, row, text):
        """Vykreslí text na řádek 0-7 (Page rozlišení, výška 8px)"""
        text = text.upper()
        current_x = x
        for char in text:
            if current_x + 8 > 128:
                break
            bitmap = FONT_8x8.get(char, FONT_8x8[' '])
            page_offset = row * 128
            for col in range(8):
                if current_x < 128:
                    self.buffer[page_offset + current_x] = bitmap[col]
                    current_x += 1

    def draw_horizontal_bar(self, x_start, row, width_max, pct):
        """Vykreslí vodorovný indikátor zaplnění do daného řádku (výška 6px)"""
        page_offset = row * 128
        fill_width = int(width_max * pct)
        
        for col in range(width_max):
            cur_x = x_start + col
            if cur_x >= 128: break
            
            if col == 0 or col == width_max - 1:
                val = 0x3F
            elif col < fill_width:
                val = 0x3F
            else:
                val = 0x21
                
            self.buffer[page_offset + cur_x] = val
